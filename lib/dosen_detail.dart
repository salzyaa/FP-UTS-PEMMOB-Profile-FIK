class DosenDetailPage extends StatelessWidget {
  final List<DosenDetail> dosenDetails;
  final String programStudi;

  const DosenDetailPage(
      {Key? key, required this.dosenDetails, required this.programStudi})
      : super(key: key);

  @override
  Widget build(BuildContext context) {
    List<DosenDetail> filteredDosenDetails = dosenDetails.where((dosen) {
      return dosen.programStudi == programStudi;
    }).toList();

    return Scaffold(
      appBar: AppBar(
        title: Text('Detail Dosen $programStudi'),
      ),
      body: ListView.builder(
        itemCount: (filteredDosenDetails.length / 2).ceil(),
        itemBuilder: (context, index) {
          int startIndex = index * 2;
          int endIndex = startIndex + 2;
          if (endIndex > filteredDosenDetails.length) {
            endIndex = filteredDosenDetails.length;
          }
          return Row(
            mainAxisAlignment: MainAxisAlignment.spaceEvenly,
            children: [
              for (int i = startIndex; i < endIndex; i++)
                Expanded(
                  child: Padding(
                    padding: EdgeInsets.all(8.0),
                    child: Column(
                      children: [
                        Image.asset(
                          filteredDosenDetails[i].imageUrl,
                          width: 100,
                          height: 100,
                        ),
                        SizedBox(height: 10),
                        Text(
                          filteredDosenDetails[i].namaDosen,
                          style: TextStyle(fontSize: 16),
                        ),
                        SizedBox(height: 5),
                        Container(
                          padding: EdgeInsets.all(8.0),
                          child: Column(
                            crossAxisAlignment: CrossAxisAlignment.start,
                            children: [
                              Container(
                                width: 150,
                                color: Colors.blue,
                                child: Center(
                                  child: Text(
                                    'Pendidikan',
                                    style: TextStyle(
                                      fontWeight: FontWeight.bold,
                                      color: Colors.white,
                                    ),
                                  ),
                                ),
                              ),
                              SizedBox(height: 5),
                              for (String pendidikan in filteredDosenDetails[i]
                                  .pendidikan
                                  .split('\n'))
                                Text(
                                  pendidikan,
                                  style: TextStyle(
                                    fontSize: 14,
                                  ),
                                ),
                            ],
                          ),
                        ),
                        SizedBox(height: 5),
                        Container(
                          padding: EdgeInsets.all(8.0),
                          child: Column(
                            crossAxisAlignment: CrossAxisAlignment.start,
                            children: [
                              Container(
                                width: 150,
                                height: 20,
                                color: Colors.blue,
                                child: Center(
                                  child: Text(
                                    'Bidang Minat & Publikasi',
                                    style: TextStyle(
                                      fontWeight: FontWeight.bold,
                                      color: Colors.white,
                                    ),
                                  ),
                                ),
                              ),
                              SizedBox(height: 5),
                              for (String bidangMinatPublikasi
                                  in filteredDosenDetails[i]
                                      .bidangMinatPublikasi
                                      .split('\n'))
                                Text(
                                  bidangMinatPublikasi,
                                  style: TextStyle(
                                    fontSize: 14,
                                  ),
                                ),
                            ],
                          ),
                        ),
                      ],
                    ),
                  ),
                ),
            ],
          );
        },
      ),
    );
  }
}
